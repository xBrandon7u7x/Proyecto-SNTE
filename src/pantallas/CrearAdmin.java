    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        conectar cc = new conectar();
        Connection cn = cc.conexion();

        String usuario, contra;
        String sql = "";
        usuario = t_usu.getText();
        contra = t_contra.getText();

        if (usuario.isEmpty() || contra.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Error de registro. Favor de llenar todos los datos.");
        } else {
            String checkSql = "SELECT COUNT(*) AS count FROM admin WHERE usuario=?";
        try {
        PreparedStatement checkStmt = cn.prepareStatement(checkSql);
        checkStmt.setString(1, usuario);
        ResultSet rs = checkStmt.executeQuery();
        rs.next();
        int count = rs.getInt("count");

        if (count > 0) {
            JOptionPane.showMessageDialog(null, "Error. Este admin ya ha sido registrado.");
        } else {
            sql = "INSERT INTO admin (usuario, contrasena) VALUES (?,?)";
            try {
                PreparedStatement pst = cn.prepareStatement(sql);
                pst.setString(1, usuario);
                pst.setString(2, contra);
                int n = pst.executeUpdate();
                if (n > 0) {
                    JOptionPane.showMessageDialog(null, "Registro completo");
                }
            } catch (SQLException ex) {
                Logger.getLogger(CrearAdmin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    } catch (SQLException ex) {
        Logger.getLogger(CrearAdmin.class.getName()).log(Level.SEVERE, null, ex);
    }
}
    }//GEN-LAST:event_jButton2ActionPerformed

