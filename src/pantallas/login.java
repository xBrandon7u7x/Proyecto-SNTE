    public void validarusuario(){
        int resultado=0;
        String pass=String.valueOf(txt_pass.getPassword());
        String usuario=txt_usuario.getText();
        String sql="Select * from admin where usuario='"+usuario+"' and contrasena='"+pass+"'";
        try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            if(rs.next()){
                resultado=1;
                if(resultado==1){
                    MenuPrincipal form= new MenuPrincipal();
                    form.setVisible(true);
                    this.dispose();
                }
                
            }else{
               JOptionPane.showMessageDialog(null,"Error de acceso, usuario no registrado o datos incorrectos"); 
            }
        }
        catch (SQLException ex) {
            Logger.getLogger(CrearAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
