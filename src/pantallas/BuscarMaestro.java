    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
    conectar cc = new conectar();
    Connection cn = cc.conexion();
    try {
        PreparedStatement pst = cn.prepareStatement("UPDATE maestro SET nombre=?, apellido_paterno=?, apellido_materno=?, sexo=?, cargo_sindical=?, nivel=?, escuela_adscripcion=?, region=?, delegacion=?, celular=?, correo=? WHERE id=?");
        pst.setString(1, txtnombre.getText());
        pst.setString(2, txtapp.getText());
        pst.setString(3, txtapm.getText());
        pst.setString(4, txtsexo.getText());
        pst.setString(5, txtcargo.getText());
        pst.setString(6, txtnivel.getText());
        pst.setString(7, txtescuela.getText());
        pst.setString(8, txtregion.getText());
        pst.setString(9, txtdelegacion.getText());
        pst.setString(10, txtcelular.getText());
        pst.setString(11, txtcorreo.getText());
        pst.setInt(12, idMaestroSeleccionado); // Utilizamos el ID del maestro seleccionado

        int resultado = pst.executeUpdate();
        if (resultado > 0) {
            JOptionPane.showMessageDialog(null, "Datos Actualizados");
            buscar("");
        } else {
            JOptionPane.showMessageDialog(null, "Error al actualizar");
        }
    } catch (SQLException ex) {
        Logger.getLogger(BuscarMaestro.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Error al actualizar");
    }
    }//GEN-LAST:event_btnactualizarActionPerformed

