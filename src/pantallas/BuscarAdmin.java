    void buscar(String valor){
        int totalRegistros = 0;
        
        String [] titulos={"#","Usuario","Contraseña"};
        String [] registros = new String [12];
        model = new DefaultTableModel(null,titulos);
        conectar cc = new conectar();
        Connection cn = cc.conexion();
        String sql="";
        
        valor = valor.trim().toLowerCase();
        
         if (valor.isEmpty()) {
        sql = "SELECT * FROM admin";
    } else {
        sql = "SELECT * FROM admin WHERE LOWER(CONCAT(usuario, ' ', contrasena)) LIKE '%" + valor + "%'";
    }
    try {
        Statement st = cn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            registros[0] = rs.getString("id");
            registros[1] = rs.getString("usuario");
            registros[2] = rs.getString("contrasena");
            
            model.addRow(registros);
            totalRegistros++;
        }
        tadmin.setModel(model);
        tadmin.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);
        tadmin.getColumnModel().getColumn(0).setPreferredWidth(100);
        tadmin.getColumnModel().getColumn(1).setPreferredWidth(200);
        txttotal.setText("Se encontraron " + totalRegistros + " registros.");
   } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, ex);
    }
    }
