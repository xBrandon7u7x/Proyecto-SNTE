    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
    int filaSeleccionada = tadmin.getSelectedRow();
    if (filaSeleccionada >= 0) {
        int idMaestro = Integer.parseInt(tadmin.getValueAt(filaSeleccionada, 0).toString());

        int opcion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar este registro?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.YES_OPTION) {
            conectar cc = new conectar();
            Connection cn = cc.conexion();
            try {
                PreparedStatement pst = cn.prepareStatement("DELETE FROM admin WHERE id=?");
                pst.setInt(1, idMaestro);

                int resultado = pst.executeUpdate();
                if (resultado > 0) {
                    JOptionPane.showMessageDialog(this, "Registro eliminado exitosamente");
                    buscar(""); // Volver a cargar los datos en la tabla
                } else {
                    JOptionPane.showMessageDialog(this, "Error al eliminar el registro");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error al eliminar el registro: " + ex.getMessage());
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "No seleccionaste un registro para eliminar");
    }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

